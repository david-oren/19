int		ft_sqrt(int nb)
{
	int i;

	if (nb <= 0)
		return (0);
	i = 0;
	while (i * i <= nb)
	{
		if (i == nb && i != 1)
			return (0);
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
